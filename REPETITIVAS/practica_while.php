<?php
/*While es una estructura repetitiva. Que corre cicloz 
a trave del lado verdadero del flujo - e ale del 
bucle cuando la sentencia es negativa
- Con While se maneja un "contador" y se usan los 
operadores lógicos de conteo  como ++ , -- 
Si ponemos el contador de forma incorrecta hace ciclos 
sin fin*/

$galletas = 0;

while ($galletas<=10) {
    echo "\n$galletas";
    //CONTADOR
    $galletas ++;
}


?>